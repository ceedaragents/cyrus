#!/usr/bin/env node

/**
 * Reads Cyrus documentation files and generates a TypeScript bundle
 * for the docs MCP tools.
 *
 * Usage:
 *   node scripts/bundle-docs.mjs <docs-directory>
 *
 * Example:
 *   node scripts/bundle-docs.mjs ../../documentation
 */

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { join, resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));

const docsDir = resolve(process.argv[2] || join(__dirname, "../../../../documentation"));

if (!existsSync(docsDir)) {
	console.error(`Documentation directory not found: ${docsDir}`);
	process.exit(1);
}

const docsJsonPath = join(docsDir, "docs.json");
if (!existsSync(docsJsonPath)) {
	console.error(`docs.json not found at: ${docsJsonPath}`);
	process.exit(1);
}

const docsConfig = JSON.parse(readFileSync(docsJsonPath, "utf-8"));

/**
 * Extract frontmatter from a markdown/MDX file.
 * Returns { title, description, content } where content is everything after frontmatter.
 */
function parseFrontmatter(raw) {
	const match = raw.match(/^---\s*\n([\s\S]*?)\n---\s*\n([\s\S]*)$/);
	if (!match) {
		// No frontmatter - extract title from first H1
		const h1Match = raw.match(/^#\s+(.+)/m);
		return {
			title: h1Match ? h1Match[1].trim() : "",
			description: "",
			content: raw,
		};
	}

	const frontmatter = match[1];
	const content = match[2];

	// Simple YAML-like parsing for title and description
	const titleMatch = frontmatter.match(/^title:\s*["']?(.+?)["']?\s*$/m);
	const descMatch = frontmatter.match(/^description:\s*["']?(.+?)["']?\s*$/m);

	return {
		title: titleMatch ? titleMatch[1].trim() : "",
		description: descMatch ? descMatch[1].trim() : "",
		content,
	};
}

/**
 * Strip MDX components to plain text for better searchability.
 */
function stripMdx(content) {
	return content
		// Remove self-closing MDX tags like <Note />, <Warning />
		.replace(/<\w+\s*\/>/g, "")
		// Remove opening MDX tags with attributes like <Card title="..." icon="..." horizontal>
		.replace(/<(Card|Steps|Step|Note|Warning|Tip|Info|Frame|img)\b[^>]*>/gi, "")
		// Remove closing MDX tags
		.replace(/<\/(Card|Steps|Step|Note|Warning|Tip|Info|Frame|img)>/gi, "")
		// Clean up excessive blank lines
		.replace(/\n{3,}/g, "\n\n")
		.trim();
}

// Build slug-to-group mapping from navigation config
const slugToGroup = new Map();
for (const group of docsConfig.navigation.groups) {
	for (const page of group.pages) {
		slugToGroup.set(page, group.group);
	}
}

// Collect all documented pages from navigation + any extra files
const allSlugs = new Set();
for (const group of docsConfig.navigation.groups) {
	for (const page of group.pages) {
		allSlugs.add(page);
	}
}

// Also check for docs not in navigation (legacy/extra)
const extraFiles = [
	"procedures",
	"procedures-guide",
	"configuration",
	"labels",
];
for (const f of extraFiles) {
	if (!allSlugs.has(f)) {
		allSlugs.add(f);
	}
}

const docs = [];

for (const slug of allSlugs) {
	// Try .mdx first, then .md
	let filePath = join(docsDir, `${slug}.mdx`);
	if (!existsSync(filePath)) {
		filePath = join(docsDir, `${slug}.md`);
	}
	if (!existsSync(filePath)) {
		console.warn(`  Skipping ${slug}: file not found`);
		continue;
	}

	const raw = readFileSync(filePath, "utf-8");
	const { title, description, content } = parseFrontmatter(raw);
	const cleanContent = stripMdx(content);
	const group = slugToGroup.get(slug) || "Other";

	docs.push({
		slug,
		title: title || slug,
		description,
		group,
		content: cleanContent,
	});
}

// Generate TypeScript output
const output = `// AUTO-GENERATED by scripts/bundle-docs.mjs — do not edit manually.
// Regenerate with: node scripts/bundle-docs.mjs <docs-directory>

export interface DocEntry {
	slug: string;
	title: string;
	description: string;
	group: string;
	content: string;
}

export const DOCS_BUNDLE: DocEntry[] = ${JSON.stringify(docs, null, "\t")};
`;

const outPath = join(__dirname, "../src/tools/docs-tools/docs-bundle.ts");
writeFileSync(outPath, output, "utf-8");

console.log(`Bundled ${docs.length} docs → ${outPath}`);
